package Bean;

public class ZhongJiang {
	private Integer sortNo;
	private String no;
	private String serialNumber;
	private String detail;
	private String zhongJiangNumber;
	private String money;
	private String totalMoney = "0";
	private String note;

	public Integer getSortNo() {
		return sortNo;
	}

	public void setSortNo(Integer sortNo) {
		this.sortNo = sortNo;
	}

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public String getSerialNumber() {
		return serialNumber;
	}

	public void setSerialNumber(String serialNumber) {
		this.serialNumber = serialNumber;
	}

	public String getDetail() {
		return detail;
	}

	public void setDetail(String detail) {
		this.detail = detail;
	}

	public String getZhongJiangNumber() {
		return zhongJiangNumber;
	}

	public void setZhongJiangNumber(String zhongJiangNumber) {
		this.zhongJiangNumber = zhongJiangNumber;
	}

	public String getMoney() {
		return money;
	}

	public void setMoney(String money) {
		this.money = money;
	}

	public String getTotalMoney() {
		return totalMoney;
	}

	public void setTotalMoney(String totalMoney) {
		this.totalMoney = totalMoney;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}
}
